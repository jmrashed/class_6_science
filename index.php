          <h2 style="margin-top: 10px; color:#13007C">10.2 REST AND MOTION ARE RELATIVE</h2>
          <p align="justify">Motion is relative to an observer or to an object. For example, a woman driving a car is not in motion relative to the car, but she is in motion relative to an observer standing on the side of the road. A similar example is a cat  sleeping on a rug. The cat appears to be at rest to an observer on Earth, but relative to an object at a fixed point  in space, the cat is moving as fast as the Earth turns. As these examples show, discussions of motion must  include a point of reference, especially when calculating measurements such as speed. For example, if a train is traveling 90 miles per hour and a man walks from the back of a train car to the front, how fast is the man moving? The answer depends on the point of reference. To another passenger in the train, the man is moving at a rate much less than 90 miles per hour. To an observer outside of the train, however, the man is moving at a  rate slightly more than 90 miles per hour.</p>
          <p align="justify">Most people consider motion relative to a fixed point on the Earth. Physicists consider that the universe has no 
fixed point, so all motion is relative to what is defined as a fixed point. In fact, this basic idea is part of  Einstein's theory of relativity.
          </p> 
              <div style="border:1px solid orange; border-radius: 10px; padding: 10px">
              
                <ul style="list-style: none;"> <span style="color:green; font-weight: bold;">TASK:</span>
                  <li>
                    <p align="justify"> Three of your friends decide to go to the field with bicycle. Tell one of your friend to stand in a    position at rest. Then you will sit on the back sit of the bicycle and tell another friend to paddle the    cycle in a straight direction. You and your friend riding on the cycle are changing position every    moment relative to the friend at rest.</p>
                  </li> 
              </ul> 
              </div>

          <p align="justify">
          Are you in motion relative to your friend at rest? Yes, you are in motion relative to the friend at rest. But is your friend who is paddling the cycle in motion relative to you? Certainly not, hence all motions are relative.
          </p>

            <br>
           <p style="color:#13007C; font-weight: bold;"> Reference frame</p>
           <p align="justify">
          The observer relative to whom motion or rest is determined is called reference frame. When you are standing on the ground, that is your frame of reference. Anything that you see, watch, or measure will be compared to the reference point of the ground. If I am standing in the back of a moving truck, the truck is now my frame of reference and everything will be measured compared to it. If you want to measure the distance of your school from your house, in this case your home is a reference frame.

          </p>
