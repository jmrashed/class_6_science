                        <h2 style="margin-top: 10px; color:#13007C">10.2 REST AND MOTION ARE RELATIVE</h2>
                        <p align="justify">Motion is relative to an observer or to an object. For example, a woman driving a car is not in motion relative to the car, but she is in motion relative to an observer standing on the side of the road. A similar example is a cat  sleeping on a rug. The cat appears to be at rest to an observer on Earth, but relative to an object at a fixed point  in space, the cat is moving as fast as the Earth turns. As these examples show, discussions of motion must  include a point of reference, especially when calculating measurements such as speed. For example, if a train is traveling 90 miles per hour and a man walks from the back of a train car to the front, how fast is the man moving? The answer depends on the point of reference. To another passenger in the train, the man is moving at a rate much less than 90 miles per hour. To an observer outside of the train, however, the man is moving at a  rate slightly more than 90 miles per hour.</p>
                        <p align="justify">Most people consider motion relative to a fixed point on the Earth. Physicists consider that the universe has no 
                            fixed point, so all motion is relative to what is defined as a fixed point. In fact, this basic idea is part of  Einstein's theory of relativity.
                        </p> 

